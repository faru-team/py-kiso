# 変数のデータ型を扱おう
value = "Hello"
print(type(value))

value = 12
print(type(value))

value = 1.0
print(type(value))

value = True
print(type(value))

value = ["white","black","red"]
print(type(value))

value = ("car", "bus", "train")
print(type(value))

value = {"12": "歳","10": "歳","15": "歳"}
print(type(value))

